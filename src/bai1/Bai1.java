package bai1;

public class Bai1 {
    public static void main(String[] args) {
        String str1 = ""; // Chuỗi rỗng
        String str2 = "Hello, world!"; // Chuỗi không rỗng

        // Kiểm tra xem chuỗi str1 có rỗng hay không
        if (str1.isEmpty()) {
            System.out.println("Chuỗi str1 là rỗng");
        } else {
            System.out.println("Chuỗi str1 không rỗng");
        }

        // Kiểm tra xem chuỗi str2 có rỗng hay không
        if (str2.isEmpty()) {
            System.out.println("Chuỗi str2 là rỗng");
        } else {
            System.out.println("Chuỗi str2 không rỗng");
        }
    }
}
